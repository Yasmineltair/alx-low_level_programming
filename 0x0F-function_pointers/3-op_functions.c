#include "3-calc.h"

/**
  * op_add - function returns sum of 2 int
  * @a: the first input int
  * @b: the second input int
  * Return: sum of 2 integers
  */

int op_add(int a, int b)
{
return (a + b);
}

/**
  * op_sub - function return subtraction between 2 int
  * @a: the first int
  * @b: the second int
  * Return: difference of a and b
  */

int op_sub(int a, int b)
{
return (a - b);
}

/**
  * op_mul - function returns the product of a and b
  * @a: the first int
  * @b: the second int
  * Return: returns the product of a and b
  */

int op_mul(int a, int b)
{
return (a * b);
}

/**
  * op_div - function  returns the result
  * of the division of a by b
  * @a: first int
  * @b: second int
  * Return:  returns the result of the division of a by b
  */

int op_div(int a, int b)
{
return (a / b);
}

/**
  * op_mod - function  returns the
  * remainder of the division of a by b
  * @a: the first int
  * @b: the second int
  * Return:  returns the remainder of the division of a by b
  */

int op_mod(int a, int b)
{
return (a % b);
}
