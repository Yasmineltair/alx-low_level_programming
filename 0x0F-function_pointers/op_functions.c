#include "3-calc.h"

/**
  * op_add - function returns sum of 2 int
  * op_sub - function returns sub of 2 int
  * op_mul - functuin returns product of 2 int
  * op_div - function returns devision of 2 int
  * op_mod - function returns the reminder 
  * of the devision of 2 int
  * @a: the first input int
  * @b: the second input int
  * Return: int
  */

int op_add(int a, int b)
{
return (a + b);
}
int op_sub(int a, int b)
{
return (a - b); 
}
int op_mul(int a, int b)
{
return (a * b);
}
int op_div(int a, int b)
{
return (a / b); 
}
int op_mod(int a, int b)
{
return (a % b);
}
